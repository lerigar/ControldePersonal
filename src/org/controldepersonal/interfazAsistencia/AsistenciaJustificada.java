    private void btnJustificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJustificarActionPerformed
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String fecha = dateFormat.format(dpAsistenciaJustificada.getDate());
        fecha = arreglaFecha(fecha);
        if(conexionactual.justificaAsistencia(id_empleado, fecha, Justificada)){            
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "No hay falta registrada");
            this.dispose();
        }
    }//GEN-LAST:event_btnJustificarActionPerformed
    private String arreglaFecha(String fecha){
        String[] temp = fecha.split("/");
        switch(temp[1]){
            case "01":
                temp[1] = "1";
            break;
            case "02":
                temp[1] = "2";
            break;
            case "03":
                temp[1] = "3";
            break;
            case "04":
                temp[1] = "4";
            break;
            case "05":
                temp[1] = "5";
            break;
            case "06":
                temp[1] = "6";
            break;
            case "07":
                temp[1] = "7";
            break;
            case "08":
                temp[1] = "8";
            break;
            case "09":
                temp[1] = "9";
            break;
            case "10":
                temp[1] = "10";
            break;
            case "11":
                temp[1] = "11";
            break;
            case "12":
                temp[1] = "12";
            break;
        }
        return temp[0]+"/"+temp[1]+"/"+temp[2];
    }

